num1 = int(input("Enter 1st Integer:"))
num2 = int(input("Enter 2nd Integer:"))
choice = str(input("Enter the operation to be performed (+,-,*,/):"))
if(choice == '+'):
   print(num1+num2)
if(choice == '='):
   print(num1-num2)
if(choice == '*'):
   print(num1*num2)
if(choice == '/'):
   print(num1/num2)
